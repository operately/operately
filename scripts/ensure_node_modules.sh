#!/usr/bin/env bash

# Reuses an existing node_modules directory when it matches package-lock.json.
# If the lockfile changed or node_modules is missing/corrupted, it rebuilds the
# install with npm ci using an isolated cache under /tmp, then retries once with
# a cleared cache to recover from transient npm cache issues in CI.

set -euo pipefail

if [ "$#" -ne 2 ]; then
  echo "Usage: $0 <project_dir> <cache_key>" >&2
  exit 1
fi

project_dir="$1"
cache_key="$2"

cd "$project_dir"

lock_file="package-lock.json"
stamp_file="node_modules/.package-lock.hash"
npm_cache_dir="/tmp/npm-cache/$cache_key"

if [ ! -f "$lock_file" ]; then
  echo "Missing $lock_file in $project_dir" >&2
  exit 1
fi

lock_hash="$(sha256sum "$lock_file" | cut -d' ' -f1)"
cached_hash="$(cat "$stamp_file" 2>/dev/null || true)"

if [ -d node_modules ] && [ "$lock_hash" = "$cached_hash" ]; then
  echo "[ensure_node_modules] Reusing $project_dir/node_modules (lockfile unchanged)"
  exit 0
fi

echo "[ensure_node_modules] Installing dependencies for $project_dir with npm ci"

rm -rf node_modules
mkdir -p "$npm_cache_dir"

if ! npm_config_cache="$npm_cache_dir" npm ci --no-audit --no-fund; then
  echo "[ensure_node_modules] Initial npm ci failed for $project_dir, clearing cache and retrying"
  rm -rf node_modules "$npm_cache_dir"
  mkdir -p "$npm_cache_dir"
  npm_config_cache="$npm_cache_dir" npm ci --no-audit --no-fund
fi

printf '%s\n' "$lock_hash" > "$stamp_file"
